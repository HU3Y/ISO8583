package org.adelbs.iso8583.xml;

public class ISOConfigMarshallerException extends Exception {

	private static final long serialVersionUID = 1L;

	public ISOConfigMarshallerException() {
	}

	public ISOConfigMarshallerException(String arg0) {
		super(arg0);
	}

	public ISOConfigMarshallerException(Throwable arg0) {
		super(arg0);
	}

	public ISOConfigMarshallerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ISOConfigMarshallerException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
